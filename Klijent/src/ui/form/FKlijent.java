    private void jbtnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSacuvajActionPerformed
        if(mode.equals("new")){
            try{
                Klijent klijent = validacija();
                String poruka = Controller.getInstance().sacuvajKlijenta(klijent);
                JOptionPane.showMessageDialog(this, poruka);
                jbtnDodaj.setEnabled(true);
                jbtnSacuvaj.setEnabled(false);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        } else {
            try{
                Klijent k = validacija();
                Controller.getInstance().promeniKlijenta(k);
                JOptionPane.showMessageDialog(this, "Sistem je zapamtio klijenta.");
                jbtnSacuvaj.setEnabled(false);
                jbtnDodaj.setEnabled(true);
            }catch(Exception ex){
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
    }//GEN-LAST:event_jbtnSacuvajActionPerformed

