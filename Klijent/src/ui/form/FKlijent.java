    private void popuniFormu() {
        
        jbtnDodaj.setEnabled(false);
        
        try{
            int  klijentId= Controller.getInstance().kreirajKlijenta();
            sifra = klijentId;
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            dispose();
        }
        
        jtxtKlijentID.setText(sifra + "");
        jcmbBanka.removeAllItems();
        
        for (Banka b : banke) {
            jcmbBanka.addItem(b);
        }
    }

    private void popuniFormu(Klijent k) {
        
        jbtnDodaj.setEnabled(false);
        jtxtKlijentID.setText(sifra+ "");
        
        jcmbBanka.removeAllItems();
        for (Banka b : banke) {
            jcmbBanka.addItem(b);
        }
        
        jtxtJmbg.setText(k.getJmbg());
        jtxtIme.setText(k.getIme());
        jtxtPrezime.setText(k.getPrezime());
        jtxtAdresa.setText(k.getAdresa());
        jtxtBrojTelefona.setText(k.getBrojTelefona());
        
    }

    private Klijent validacija() throws Exception {
        
        if(jtxtJmbg.getText().isEmpty() || jtxtIme.getText().isEmpty() || jtxtPrezime.getText().isEmpty() || 
                jtxtAdresa.getText().isEmpty() || jtxtBrojTelefona.getText().isEmpty())
            throw new Exception("Sva polja moraju biti popunjena.");
        
        String jmbg = jtxtJmbg.getText();
        
        if(jmbg.length() != 13 || !jmbg.matches("[0-9]+"))
            throw new Exception("JMBG mora imati tacno 13 cifara i ne sme sadrzati nista drugo sem cifara.");
        
        String ime = jtxtIme.getText();
        String prezime = jtxtPrezime.getText();
        String adresa = jtxtAdresa.getText();
        String brojTelefona = jtxtBrojTelefona.getText();
        
        if(!brojTelefona.matches("[0-9]+"))
            throw new Exception("Broj telefona sme sadrzati samo cifre.");
        
        Banka banka = (Banka) jcmbBanka.getSelectedItem();
        
        return new Klijent(sifra, jmbg, ime, prezime, adresa, brojTelefona, banka);
    }

    private void resetujFormu() {
        
        try{
            int klijentovid = Controller.getInstance().kreirajKlijenta();
            sifra = klijentovid;
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex.getMessage());
            dispose();
        }
        jtxtKlijentID.setText(sifra + "");
        jtxtJmbg.setText("");
        jtxtIme.setText("");
        jtxtPrezime.setText("");
        jtxtAdresa.setText("");
        jtxtBrojTelefona.setText("");
    }
