    private void popuniFormu() {
        
        List<Automobil> automobili;
        
        try{
            automobili = Controller.getInstance().vratiAutomobile(new Automobil());
            model = new AutomobilTableModel(automobili);
            jTable1.setModel(model);
            nit = new nitAutomobil(model,jcbMarka,jcbGorivo,jtxtMarka,jtxtGorivo,this);
            nit.start();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
