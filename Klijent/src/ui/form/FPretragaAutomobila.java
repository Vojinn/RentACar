    private void jbtnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnIzmeniActionPerformed
        
        int red = jTable1.getSelectedRow();
        if(red != -1){
            Automobil a = model.getAutomobil(red);
            new FAutomobil(a).setVisible(true);
        }else{
            JOptionPane.showMessageDialog(this, "Selektujte klijenta čije podatke menjate.");
        }
    }//GEN-LAST:event_jbtnIzmeniActionPerformed

    private void jbtnIznajmiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnIznajmiActionPerformed
        
        int red = jTable1.getSelectedRow();
        
        if(red != -1){
            Automobil a = model.getAutomobil(red);
            if(a.isRaspoloziv() == false){
                JOptionPane.showMessageDialog(this, "Izabrani automobil je već zadužen.");
                return;
            }
            try {
                int iznajmljivanjeID = Controller.getInstance().kreirajNovoIznajmljivanje();
                FIznajmljivanje forma = new FIznajmljivanje(a,iznajmljivanjeID);
                forma.setVisible(true);
                this.dispose();
                JOptionPane.showMessageDialog(this, "Unesite datum razduživanja.");
            } catch (IOException ex) {
                Logger.getLogger(FPretragaAutomobila.class.getName()).log(Level.SEVERE, null, ex);
            } catch (Exception ex) {
                Logger.getLogger(FPretragaAutomobila.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }else{
            JOptionPane.showMessageDialog(this, "Selektujte automobil za iznajmljivanje.");
        }
    }//GEN-LAST:event_jbtnIznajmiActionPerformed

