    private void jbtnSacuvajAutomobilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSacuvajAutomobilActionPerformed
        if(mode.equals("new")){
            try{
                Automobil automobil = validacija();
                String poruka = Controller.getInstance().sacuvajAutomobil(automobil);
                JOptionPane.showMessageDialog(this, poruka);
                jbtnSacuvajAutomobil.setEnabled(false);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        } else {
            try{
                Automobil a = validacija();
                Controller.getInstance().promeniAutomobil(a);
                JOptionPane.showMessageDialog(this, "Sistem je zapamtio automobil.");
                jbtnSacuvajAutomobil.setEnabled(false);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
        
    }//GEN-LAST:event_jbtnSacuvajAutomobilActionPerformed

