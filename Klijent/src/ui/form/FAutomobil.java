    private void popuniFormu(Automobil a) {
        jbtnSacuvajAutomobil.setEnabled(true);
        jtxtAutomobilID.setText(automobilID+ " ");
        
        jtxtRegistarskiBroj.setText(a.getRegistarskiBroj());
        jtxtMarka.setText(a.getMarka());
        jtxtModel.setText(a.getModel());
        jtxtGodinaProizvodnje.setText(String.valueOf(a.getGodinaProizvodnje()));
        jtxtBrojVrata.setText(String.valueOf(a.getBrojVrata()));
        jcmbGorivo.setSelectedItem(a.getGorivo());
        jtxtCenaNaDan.setText(String.valueOf(a.getCenaNaDan()));
        if(a.isRaspoloziv()){
            jcbDa.setSelected(true);
            jcbNe.setSelected(false);
        }else{
            jcbDa.setSelected(false);
            jcbNe.setSelected(true);
        }
            
        
    }

