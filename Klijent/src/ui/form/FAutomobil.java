    private Automobil validacija() throws Exception {
        
        if(jtxtRegistarskiBroj.getText().isEmpty() || jtxtMarka.getText().isEmpty() || jtxtModel.getText().isEmpty() || 
                jtxtGodinaProizvodnje.getText().isEmpty() || jtxtBrojVrata.getText().isEmpty() || jtxtCenaNaDan.getText().isEmpty() || 
                (jcbDa.isSelected() && jcbNe.isSelected()) || (!jcbDa.isSelected() && !jcbNe.isSelected()))
            throw new Exception("Sva polja moraju biti popunjena.");
        
        if(!jtxtBrojVrata.getText().matches("[0-9]+") || !jtxtGodinaProizvodnje.getText().matches("[0-9]+") || !jtxtCenaNaDan.getText().matches("[0-9]+"))
            throw new Exception("Broj vrata,godina proizvodnje i cena na dan mogu sadrzati samo brojeve.");
        
        String registarskiBroj = jtxtRegistarskiBroj.getText();
        String marka = jtxtMarka.getText();
        String model = jtxtModel.getText();
        int godinaProizvodnje = Integer.parseInt(jtxtGodinaProizvodnje.getText());
        int brojVrata = Integer.parseInt(jtxtBrojVrata.getText());
        String gorivo  = (String) jcmbGorivo.getSelectedItem();
        int cenaNaDan = Integer.parseInt(jtxtCenaNaDan.getText());
        boolean raspoloziv = true;
        
        if(jcbNe.isSelected())
            raspoloziv = false;
        
        return new Automobil(automobilID, registarskiBroj, marka, model, godinaProizvodnje, brojVrata, gorivo, cenaNaDan, raspoloziv);
        
    }
